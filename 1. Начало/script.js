/*
    Переменные
    Хранят информацию
    Имя переменной должно содержать только латинские символы, цифры и знак подчёркивания.
    Для создания переменной используют операторы: let или var.

*/

// Переменная
let name;

// Запрос у пользователя ввода информации
name = prompt('Введите ваше имя')

// Вывод информации в консоль браузера
console.log(`Привет, ${name}!`);

// Вывод информации в документ (html)
document.write(`Привет, ${name}!`);